def to_excel(df):
    """保留原始需求：將 DataFrame 轉換為 Excel 二進位格式"""
    output = io.BytesIO()
    with pd.ExcelWriter(output, engine='openpyxl') as writer:
        df.to_excel(writer, index=False, sheet_name='Data')
    return output.getvalue()

